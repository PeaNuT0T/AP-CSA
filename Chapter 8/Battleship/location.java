// Builder Class for Battleship by Talon Huang
public class location
{
  private String token = " ";

  public location()
  {
  }
 
  public String toString()
  {
    return token;
  }
  public String getToken()
  {
    return token;
  }
  public void setH()
  {
    token = "H";
  }
  public void setM()
  {
    token = "M";
  }
  public void setS()
  {
    token = "S";
  }
}